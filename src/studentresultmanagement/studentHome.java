         try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root", "Rudhra@123");
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("select * from student inner join result where student.rollno='" + rollno + "' and result.rollno='"+ rollno +"'");
            if(rs.next()){
                jTextField4.setText(rs.getString(2));
                jTextField5.setText(rs.getString(4));
                jTextField6.setText(rs.getString(5));
                jTextField10.setText(rs.getString(3));
                jTextField11.setText(rs.getString(1));
                jTextField12.setText(rs.getString(6));
                
                jTextField1.setText(rs.getString(8));
                jTextField14.setText(rs.getString(9));
                jTextField15.setText(rs.getString(10));
                jTextField16.setText(rs.getString(11));
                jTextField17.setText(rs.getString(12));
                
                int s1=Integer.parseInt(jTextField1.getText());
                int s2=Integer.parseInt(jTextField14.getText());
                int s3=Integer.parseInt(jTextField15.getText());
                int s4=Integer.parseInt(jTextField16.getText());
                int s5=Integer.parseInt(jTextField17.getText());
                
                int result=s1+s2+s3+s4+s5;
                String s=String.valueOf(result);
                jTextField18.setText(s);
                
                if(s1<=30 || s2<=30 || s3<=30 || s4<=30 || s5<=30){
                    jTextField19.setText("fail");
                }else{
                    jTextField19.setText("pass");
                }
                
                        
            }    
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e.toString());
        }
        
